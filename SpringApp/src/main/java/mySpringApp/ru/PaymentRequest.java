package mySpringApp.ru;

public class PaymentRequest {

    private String name;
    private int age;

    public PaymentRequest() {
    }

    public PaymentRequest(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
